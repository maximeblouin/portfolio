% Create a sample dataset
data = table(...
    [0.60; 0.65; 0.70; 0.75; 0.80], ...
    [0.60; 0.62; 0.65; 0.68; 0.70], ...
    [0.60; 0.63; 0.67; 0.70; 0.73], ...
    [0.60; 0.61; 0.63; 0.66; 0.70], ...
    'VariableNames', {'Index_Return', 'StockA_Return', 'StockB_Return', 'StockC_Return'});

% Fit linear regression model
model = fitlm(data, 'Index_Return ~ StockA_Return + StockB_Return + StockC_Return');

% Retrieve the coefficients (weights)
weights = model.Coefficients.Estimate(2:end);
disp(weights)
